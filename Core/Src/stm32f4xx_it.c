/**
  * @brief This function handles USART1 global interrupt.
  */
void USART1_IRQHandler(void)
{
  /* USER CODE BEGIN USART1_IRQn 0 */
 if (__HAL_UART_GET_FLAG(&huart1, UART_FLAG_IDLE)) {
      __HAL_UART_CLEAR_IDLEFLAG(&huart1);
      HAL_UART_DMAStop(&huart1);
      uint16_t recv_len = uartGroup.L1.buff - __HAL_DMA_GET_COUNTER(huart1.hdmarx);
      register uint16_t i = 0;
      while (recv_len - i >= uartGroup.L1.len) {
          if (uartGroup.L1.Receive_data[i] == 0xB4 && uartGroup.L1.Receive_data[i+1] == 0x69) {
              memcpy(uartGroup.L1.prcData, &uartGroup.L1.Receive_data[i], uartGroup.L1.len);
              i += uartGroup.L1.len;
          } else {
              i++;
          }
      }
      memset(uartGroup.L1.Receive_data, 0, uartGroup.L1.buff);
      HAL_UART_Receive_DMA(&huart1, uartGroup.L1.Receive_data, uartGroup.L1.buff);
  }
  /* USER CODE END USART1_IRQn 0 */
  HAL_UART_IRQHandler(&huart1);
  /* USER CODE BEGIN USART1_IRQn 1 */

  /* USER CODE END USART1_IRQn 1 */
}

